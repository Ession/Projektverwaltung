    private void jButtonAbschickenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAbschickenActionPerformed
        if (jTextAreaKommentar.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Bitte geben sie einen Kommentar ab.", "Eingabefehler", JOptionPane.ERROR_MESSAGE);
        }
        else {
            DataP d = new DataP();
            d.updateProjektKommentar(projekt.getTitel(), jTextAreaKommentar.getText());
            d.updateProjektStatus(projekt.getTitel(), "abgelehnt");
            parent.parent.update();
            parent.dispose();
            this.dispose();
        }
            
    }//GEN-LAST:event_jButtonAbschickenActionPerformed

